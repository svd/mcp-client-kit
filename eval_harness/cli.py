"""eval-kit CLI — ties together verify, runner_gen, report, and merge-session."""
from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path


# ── Emoji helpers ─────────────────────────────────────────────────────────────

_STATUS_ICON = {
    "pass": "✅",
    "fail": "❌",
    "skip": "⏭",
    "error": "⚠️",
}

_VERDICT_ICON = {
    "pass": "✅",
    "partial": "⚠️",
    "fail": "❌",
    "error": "⚠️",
}


# ── Subcommand implementations ────────────────────────────────────────────────


def cmd_verify(args: argparse.Namespace) -> int:
    """eval-kit verify <server>"""
    try:
        from eval_harness.manifest import get_server
        from eval_harness.verify import verify_server
    except ImportError as exc:
        print(f"ImportError: {exc}\nInstall the package first.", file=sys.stderr)
        return 1

    base_dir = Path(args.base_dir)
    manifest_path = Path(args.manifest)

    try:
        spec = get_server(args.server, path=manifest_path)
    except KeyError as exc:
        print(f"Error: {exc}", file=sys.stderr)
        return 1

    print(f"Verifying {spec.name}...")

    result = verify_server(spec, base_dir=base_dir)

    checks: dict[str, str] = result.get("checks", {})
    details: dict[str, str] = result.get("check_details", {})

    for check_name, status in checks.items():
        icon = _STATUS_ICON.get(status, "?")
        detail = details.get(check_name, "")
        suffix = f"  {detail}" if detail else ""
        print(f"{icon} {check_name:<14} {status}{suffix}")

    verdict = result.get("verdict", "error")
    verdict_icon = _VERDICT_ICON.get(verdict, "?")
    print(f"\nVerdict: {verdict_icon} {verdict}")

    server_dir = base_dir / spec.name
    result_json = server_dir / "result.json"
    print(f"Written: {spec.name}/result.json")

    if verdict == "pass":
        return 0
    if verdict == "error":
        print(f"Warning: verify returned verdict=error for {spec.name}", file=sys.stderr)
        return 0
    # "partial" or "fail"
    return 1


def cmd_runner(args: argparse.Namespace) -> int:
    """eval-kit runner <server>"""
    try:
        from eval_harness.manifest import get_server
        from eval_harness.runner_gen import generate_runner
    except ImportError as exc:
        print(f"ImportError: {exc}\nInstall the package first.", file=sys.stderr)
        return 1

    base_dir = Path(args.base_dir)
    manifest_path = Path(args.manifest)

    try:
        spec = get_server(args.server, path=manifest_path)
    except KeyError as exc:
        print(f"Error: {exc}", file=sys.stderr)
        return 1

    server_dir = base_dir / spec.name
    shapes_path = server_dir / f"{spec.name}.shapes.json"

    shapes: dict = {}
    if shapes_path.exists():
        try:
            shapes = json.loads(shapes_path.read_text(encoding="utf-8"))
        except (OSError, json.JSONDecodeError) as exc:
            print(f"Warning: could not load {shapes_path}: {exc}", file=sys.stderr)
    else:
        print(
            f"Warning: {shapes_path} not found — generating minimal runner.",
            file=sys.stderr,
        )

    generate_runner(spec, server_dir, shapes)
    print(f"Runner written: {spec.name}/run.py")
    return 0


def cmd_report(args: argparse.Namespace) -> int:
    """eval-kit report"""
    try:
        from eval_harness.report import generate_report, find_results
    except ImportError as exc:
        print(f"ImportError: {exc}\nInstall the package first.", file=sys.stderr)
        return 1

    base_dir = Path(args.base_dir)
    out_path = Path(args.out)

    # Count servers for the summary line
    result_paths = find_results(base_dir)
    n_servers = len(result_paths)

    generate_report(base_dir=base_dir, out_path=out_path)
    print(f"Report written: {out_path} ({n_servers} servers)")
    return 0


def cmd_merge_session(args: argparse.Namespace) -> int:
    """eval-kit merge-session <server>"""
    base_dir = Path(args.base_dir)
    server_dir = base_dir / args.server

    draft_path = server_dir / "session-overview.draft.md"
    analyzer_path = server_dir / "session-analyzer.md"

    if not draft_path.exists():
        print(
            f"Error: {draft_path} not found — run session-overview drafting first.",
            file=sys.stderr,
        )
        return 1

    draft_content = draft_path.read_text(encoding="utf-8")

    if analyzer_path.exists():
        analyzer_content = analyzer_path.read_text(encoding="utf-8")
    else:
        print(
            f"Warning: {analyzer_path} not found — using placeholder.",
            file=sys.stderr,
        )
        analyzer_content = None

    analyzer_section = (
        analyzer_content
        if analyzer_content is not None
        else "(session-analyzer.md not found)"
    )

    merged = (
        "<!-- session-overview.md — merged from draft narrative + session-analyzer output -->\n"
        "<!-- Generated by eval-kit merge-session -->\n"
        "\n"
        f"{draft_content}\n"
        "\n"
        "---\n"
        "\n"
        "## Session analyzer output\n"
        "\n"
        f"{analyzer_section}\n"
    )

    out_path = server_dir / "session-overview.md"
    out_path.write_text(merged, encoding="utf-8")
    print(f"Merged: {args.server}/session-overview.md")
    return 0


# ── Parser ────────────────────────────────────────────────────────────────────


def _build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        prog="eval-kit",
        description="Evaluation harness CLI for mcp-client-kit.",
    )
    sub = parser.add_subparsers(dest="command", metavar="<subcommand>")
    sub.required = True

    # --- verify ---
    p_verify = sub.add_parser("verify", help="Verify a server's generated artifacts.")
    p_verify.add_argument("server", help="Server name (must appear in manifest).")
    p_verify.add_argument(
        "--base-dir", default=".", metavar="DIR",
        help="Project root where <server>/ folders live (default: .).",
    )
    p_verify.add_argument(
        "--manifest", default="servers/servers.toml", metavar="PATH",
        help="Path to servers.toml manifest (default: servers/servers.toml).",
    )

    # --- runner ---
    p_runner = sub.add_parser("runner", help="Generate a sample runner script for a server.")
    p_runner.add_argument("server", help="Server name (must appear in manifest).")
    p_runner.add_argument(
        "--base-dir", default=".", metavar="DIR",
        help="Project root where <server>/ folders live (default: .).",
    )
    p_runner.add_argument(
        "--manifest", default="servers/servers.toml", metavar="PATH",
        help="Path to servers.toml manifest (default: servers/servers.toml).",
    )

    # --- report ---
    p_report = sub.add_parser("report", help="Generate EVAL_REPORT.md from result.json files.")
    p_report.add_argument(
        "--base-dir", default=".", metavar="DIR",
        help="Project root where <server>/ folders live (default: .).",
    )
    p_report.add_argument(
        "--out", default="doc/EVAL_REPORT.md", metavar="PATH",
        help="Output path for the report (default: doc/EVAL_REPORT.md).",
    )

    # --- merge-session ---
    p_merge = sub.add_parser(
        "merge-session",
        help="Merge session-overview.draft.md + session-analyzer.md into session-overview.md.",
    )
    p_merge.add_argument("server", help="Server name (subfolder under base-dir).")
    p_merge.add_argument(
        "--base-dir", default=".", metavar="DIR",
        help="Project root where <server>/ folders live (default: .).",
    )

    return parser


# ── Entry point ───────────────────────────────────────────────────────────────


def main(argv: list[str] | None = None) -> None:
    parser = _build_parser()
    args = parser.parse_args(argv)

    dispatch = {
        "verify": cmd_verify,
        "runner": cmd_runner,
        "report": cmd_report,
        "merge-session": cmd_merge_session,
    }

    handler = dispatch[args.command]
    code = handler(args)
    sys.exit(code)
